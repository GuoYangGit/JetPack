@Singleton
class ApiService @Inject constructor() {
    companion object {
        const val SERVER_URL = "https://wanandroid.com/"
    }

    suspend fun getArterialList(page: Int): ArterialBean {
        return RxHttp.get("${SERVER_URL}article/list/$page/json")
            .toResponse<ArterialBean>()
            .await()
    }
}