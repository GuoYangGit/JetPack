/**
 * 获取进程号对应的进程名
 *
 * @param pid 进程号
 * @return 进程名
 */
fun getProcessName(pid: Int): String? {
    try {
        BufferedReader(FileReader("/proc/$pid/cmdline")).use { reader ->
            var processName = reader.readLine()
            if (processName.isNotEmpty()) {
                processName = processName.trim { it <= ' ' }
            }
            return processName
        }
    } catch (e: Exception) {
        e.printStackTrace()
        return null
    }
}